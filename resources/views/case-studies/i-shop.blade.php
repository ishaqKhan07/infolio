                            The same was done for riders as people who ordered products could rate the riders based on their behavior.<br><br>

                            The iShop team approved the following design because of the simplicity it entails. The shops are available on the home screen after a user signs-in. Users can even check the products they want to order by directly clicking on the store’s menu or account.<br><br>

