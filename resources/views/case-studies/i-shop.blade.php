                        <h4><span style="font-weight: bold;">Purpose</span> <span style="font-weight: normal;">of iShop Project</span></h4>
                        <p>iShop is a grocery shopping and booking app for the UAE market. The app offers a quick and easy way for people to order their food in restaurants. And specialized offers for homemade small food businesses.</p>
                    </div>
                    <div class="col-lg-12 col-md-6 pt-80">
                        <h4><span style="font-weight: bold;">Problem:</span> <span style="font-weight: normal;">A Localized Design</span></h4>
                        <p>iShop found a gap in the online market. The simple question was: What if someone wanted to order products from multiple stores at once?<br><br>

                            Each store offered a delivery service but the timing of delivery for each store was different. Moreover, each store charged a small fee for delivering the goods. The cost doubled when a person was ordering products from multiple stores. The solution was simple. Make a simple app where riders can take orders from all the stores in the area and deliver them to the user’s doorstep.<br><br>

                            The design of the app needed to be simple so that everyone can understand the app really quickly.</p>
                    </div>
